package Ejercicio2;

public class Calculadora {
    public int metodoSuma(int a, int b){
       return a+b;
    }
   public int metodoResta(int a, int b) {
        return b-a;
    }
   public int metodomultiplicacion (int a, int b){
        return a*b;
    }
    public int metododivision(int a, int b) {
        return b/a;
    }
}

